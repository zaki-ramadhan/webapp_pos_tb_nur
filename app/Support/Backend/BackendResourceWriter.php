                $lines[] = ['account_id' => $record->primary_account_id, 'description' => 'Hutang Usaha', 'debit_amount' => 0.00, 'credit_amount' => $record->total_amount, 'total_amount' => $record->total_amount, 'sort_order' => $sortOrder++];
            }

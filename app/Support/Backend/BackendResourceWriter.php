    /**
     * Cek apakah nomor dokumen adalah nomor otomatis (generated).
     */
    protected function isAutoGeneratedNumber(string $number, string $resourceKey): bool
    {
        $number = trim($number);
        if ($number === '') {
            return true;
        }

        // Format titik lama: PREFIX.YYYY.MM.DD.HHMMSS
        if (preg_match('/^[A-Z]+(\.[A-Z]+)?\.\d{4}\.\d{2}\.\d{2}\.\d{6}$/', $number)) {
            return true;
        }

        // Format strip baru: PREFIX-YYYYMMDD-HHMMSS
        if (preg_match('/^[A-Z]+(\.[A-Z]+)?-\d{8}-\d{6}$/', $number)) {
            return true;
        }

        return false;
    }

    /**
     * Generate nomor dokumen sekuensial urut ala Accurate.
     */
    protected function generateNextSequentialNumber(string $resourceKey, ?string $entryDate): string
    {
        $prefixes = [
            'sales-quotes' => 'SQ',
            'sales-orders' => 'SO',
            'sales-deliveries' => 'SD',
            'sales-invoices' => 'SI',
            'sales-returns' => 'SR',
            'purchase-orders' => 'PO',
            'goods-receipts' => 'GR',
            'purchase-invoices' => 'PI',
            'purchase-returns' => 'PR',
            'payroll-entries' => 'EPY',
            'expense-entries' => 'EXP',
            'general-journals' => 'JV',
            'cash-payments' => 'CP',
            'cash-receipts' => 'CR',
            'bank-transfers' => 'BT',
            'sales-deposits' => 'DP',
            'purchase-payments' => 'PP',
            'sales-receipts' => 'SR',
            'item-requests' => 'IR',
        ];

        $prefix = $prefixes[$resourceKey] ?? 'DOC';
        
        $date = $entryDate ? \Carbon\Carbon::parse($entryDate) : now();
        $year = $date->format('Y');
        $month = $date->format('m');
        
        // Cari nomor terakhir di bulan dan tahun yang sama: e.g. PREFIX.YYYY.MM.%
        $latest = DB::table('operation_documents')
            ->where('document_type', $this->getDocumentTypeFromResource($resourceKey))
            ->where('document_number', 'like', "{$prefix}.{$year}.{$month}.%")
            ->orderBy('id', 'desc')
            ->get()
            ->first(function ($doc) {
                // Hanya ambil yang berformat PREFIX.YYYY.MM.00001 (4 bagian)
                $parts = explode('.', $doc->document_number);
                return count($parts) === 4 && is_numeric(end($parts)) && strlen(end($parts)) === 5;
            });

        $nextSeq = 1;
        if ($latest) {
            $parts = explode('.', $latest->document_number);
            $lastPart = end($parts);
            if (is_numeric($lastPart)) {
                $nextSeq = ((int) $lastPart) + 1;
            }
        }

        $seqString = str_pad($nextSeq, 5, '0', STR_PAD_LEFT);
        return "{$prefix}.{$year}.{$month}.{$seqString}";
    }

